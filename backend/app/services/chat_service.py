"""Follow-up chat orchestration (PLAN_PART_5 §1).

Holds the in-process :class:`ChatStreamManager` (the chat analogue of the workflow
``event_bus``): the assistant reply is generated by a background task that pushes
token deltas into a per-message buffer, while the SSE endpoint replays the buffer
and tails live updates — giving refresh/reconnect resumability via ``sinceSeq``.

A turn is grounded by :mod:`app.retrieval.pack`: report markdown + a BM25-ranked
source pack + a sliding window of history. After streaming, ``[i]`` markers are
mapped back to source URLs for the UI citations row.
"""

